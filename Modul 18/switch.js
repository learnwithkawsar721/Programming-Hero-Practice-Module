var color = "";

switch (color) {
    case 'red':
        console.log("This is red Color");
        break;
    case 'yellow':
        console.log("This is Yellow Color");
        break;
    case 'green':
        console.log("This is green Color");
        break;
    case 'blue':
        console.log("This is blue Color");
        break;
    default:
        console.log("This is Black Color");
        break;
    
    
}